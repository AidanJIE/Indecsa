package com.example.indecsa.models;
public class CapHumDto {

    private Integer idCapHum;
    private String correoCapHum;
    private String contraseñaCapHum;

    public Integer getIdCapHum() {
        return idCapHum;
    }

    public void setIdCapHum(Integer idCapHum) {
        this.idCapHum = idCapHum;
    }

    public String getCorreoCapHum() {
        return correoCapHum;
    }

    public void setCorreoCapHum(String correoCapHum) {
        this.correoCapHum = correoCapHum;
    }

    public String getContraseñaCapHum() {
        return contraseñaCapHum;
    }

    public void setContraseñaCapHum(String contraseñaCapHum) {
        this.contraseñaCapHum = contraseñaCapHum;
    }
}